tuple = (101, 102, 103, 104, 105, 106, 107, 108, 109, 110)
print(tuple)              # prints entire tuple
print(tuple[3])           # element at index 3
print(tuple[0:3])         # elements from index 0 to 2
print(tuple[0:6:2])       # elements from 0 to 5 with step 2
print(tuple[:-1])         # all elements except last
print(tuple[:])           # entire tuple
print(tuple[-5:])         # last 5 elements

print(102 in tuple)
print("yash" in tuple)